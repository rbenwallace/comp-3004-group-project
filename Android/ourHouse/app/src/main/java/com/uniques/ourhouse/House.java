package com.uniques.ourhouse;

import java.util.ArrayList;
import java.util.Date;

public class House {
    private String name;
    private int id;
    private boolean showLate;
    private boolean showDifficulty;
    private ArrayList<String> roommates;

    public House(String name, int id, boolean showLate, boolean showDifficulty) {
        this.name = name;
        this.id = id;
        this.showLate = showLate;
        this.showDifficulty = showDifficulty;
        this.roommates = roommates;
    }
}
